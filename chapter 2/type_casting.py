# Type Casting:
# The conversion of the one datatype to another datatype is called types casting#


# 1. Explicit conversion- occurs through the developers
# 2. Implicit conversion- occurs through the itself python



a='20'
b='40'

print(int(a)+int(b))   # -----Explicit conversion because we convert string to the integer


c=50
d=50
  
print(c+d)    # -----Implicit conversion because the python itself take as interger


